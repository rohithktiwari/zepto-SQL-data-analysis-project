CREATE DATABASE ZEPTO_PROJECT;
USE ZEPTO_PROJECT;




CREATE TABLE ZEPTO_TABLE(
  CATEGORY VARCHAR(50),
  NAME VARCHAR(120) NOT NULL,
  MRP DECIMAL(10,3),
  DISCOUNTPERCENT INT,
  AVAILABLEQUANTITY DECIMAL(10,3),
  DISCOUNTEDSELLINGPRICE DECIMAL(10,3),
  WEIGHTINGMS DECIMAL(10,3),
  OUTOFSTOCK VARCHAR(10),
  QUANTITY NUMERIC(10,3)
);



