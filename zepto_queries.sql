-- Q1. Find the top 10 best-value products based on the discount percentage.

SELECT NAME,mrp,DISCOUNTPERCENT 
FROM ZEPTO_TABLE
ORDER BY DISCOUNTPERCENT DESC
LIMIT 10;



-- Q2. What are the Products with High MRP but Out of Stock

SELECT DISTINCT(NAME), MRP,OUTOFSTOCK
FROM ZEPTO_TABLE
WHERE OUTOFSTOCK = 'TRUE' AND MRP > 300
ORDER BY MRP DESC;



-- Q3Calculate Estimated Revenue for each category

SELECT CATEGORY,
SUM(DISCOUNTEDSELLINGPRICE*AVAILABLEQUANTITY) AS Total_revenue
FROM ZEPTO_TABLE
GROUP BY CATEGORY
ORDER BY Total_revenue;



-- Q4. Find all products where MRP is greater than 500 and discount is less than 10%

SELECT DISTINCT(NAME),MRP,DISCOUNTPERCENT
FROM ZEPTO_TABLE
WHERE MRP > 500 AND DISCOUNTPERCENT < 10
ORDER BY MRP DESC,DISCOUNTPERCENT DESC;



-- Q5. Identify the top 5 categories offering the highest average discount percentage

SELECT CATEGORY,
round(AVG(DISCOUNTPERCENT),2) AS highest_average_discount_percentage
FROM ZEPTO_TABLE
GROUP BY CATEGORY
ORDER BY highest_average_discount_percentage DESC
LIMIT 5;




-- Q6. Find the price per gram for products above 100g and sort by best value.
SELECT * FROM ZEPTO_TABLE;
SELECT DISTINCT(NAME),WEIGHTINGMS,DISCOUNTEDSELLINGPRICE,
ROUND(DISCOUNTEDSELLINGPRICE / WEIGHTINGMS,2) AS PRICE_PER_GRAM
FROM ZEPTO_TABLE
WHERE WEIGHTINGMS >= 100
ORDER BY PRICE_PER_GRAM ;




-- Q7. Group the products into categories like Low, Medium, Bulk.

SELECT DISTINCT(NAME), WEIGHTINGMS,
CASE WHEN WEIGHTINGMS < 1000 THEN 'LOW'
     WHEN WEIGHTINGMS < 5000 THEN 'MEDIUM'
     ELSE 'BULK'
     END AS WEIGHT_CATEGORY
FROM ZEPTO_TABLE
ORDER BY WEIGHT_CATEGORY;



-- Q8. What is the Total Inventory Weight Per Category
SELECT * FROM ZEPTO_TABLE;
SELECT DISTINCT(CATEGORY),ROUND(SUM(WEIGHTINGMS * AVAILABLEQUANTITY),2) AS TOTAL_WEIGHTS
FROM ZEPTO_TABLE
GROUP BY CATEGORY
ORDER BY TOTAL_WEIGHTS;




